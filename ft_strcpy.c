char	*ft_strcpy(char *dst, const char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	if (src[i] == '\0')
		dst[i] = src[i];
	return (dst);
}
